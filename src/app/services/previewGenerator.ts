// Converts project data into self-contained, interactive HTML screens.
// Uses data-nav attributes and a global click listener for safe iframe navigation.

interface PreviewInput {
  appName: string;
  appDescription: string;
  routes: Array<{ page_name: string; route_path: string; purpose: string | null }>;
  features: Array<{ feature_name: string; priority: string | null }>;
  databaseTables: Array<{ table_name: string; purpose: string | null }>;
}

export function generatePreviewHTML(
  input: PreviewInput,
  currentPath: string = "/"
): string {
  const normalizedPath = currentPath.startsWith("/")
    ? currentPath
    : `/${currentPath}`;

  const navLinks = input.routes
    .filter((r) => r.route_path !== "/")
    .slice(0, 4)
    .map(
      (r) =>
        `<a data-nav="${encodeURIComponent(r.route_path)}" class="px-3 py-2 rounded-lg text-sm font-medium ${
          normalizedPath === r.route_path
            ? "bg-slate-800 text-white"
            : "text-slate-300 hover:text-white hover:bg-slate-800/50"
        } transition cursor-pointer">${escapeHtml(r.page_name)}</a>`
    )
    .join("\n");

  const header = `
    <nav class="border-b border-slate-800 bg-slate-900 px-6 py-4">
      <div class="max-w-6xl mx-auto flex items-center justify-between">
        <a data-nav="%2F" class="text-xl font-bold text-white flex items-center gap-2 cursor-pointer">
          <span class="text-orange-500">⚡</span> ${escapeHtml(input.appName)}
        </a>
        <div class="flex items-center gap-2">
          ${navLinks}
        </div>
      </div>
    </nav>
  `;

  const footer = `
    <footer class="border-t border-slate-800 bg-slate-950 py-8 text-center text-slate-500 text-xs mt-auto">
      <p>© ${new Date().getFullYear()} ${escapeHtml(input.appName)} — Generated by S2KDOTZA App Builder Studio.</p>
    </footer>
  `;

  // Global navigation script - included in every screen
  const navScript = `
    <script>
      document.addEventListener('click', function(e) {
        const target = e.target.closest('[data-nav]');
        if (target) {
          e.preventDefault();
          const path = target.getAttribute('data-nav');
          window.location.href = '?path=' + path;
        }
      });
    </script>
  `;

  let bodyContent = "";
  let isAuthScreen = false;

  switch (normalizedPath) {
    case "/":
      bodyContent = renderHomeScreen(input);
      break;
    case "/auth":
    case "/login":
    case "/signup":
      bodyContent = renderAuthScreen(input);
      isAuthScreen = true;
      break;
    case "/dashboard":
      bodyContent = renderDashboardScreen(input);
      break;
    case "/settings":
      bodyContent = renderSettingsScreen(input);
      break;
    default:
      bodyContent = renderGenericScreen(input, normalizedPath);
      break;
  }

  if (isAuthScreen) {
    // Auth screen is self-contained
    return bodyContent;
  }

  return `<!DOCTYPE html>
<html lang="en" class="h-full">
<head>
  <meta charset="UTF-8" />
  <meta name="viewport" content="width=device-width, initial-scale=1.0" />
  <title>${escapeHtml(input.appName)} - Preview</title>
  <script src="https://cdn.tailwindcss.com"></script>
</head>
<body class="bg-slate-950 text-white flex flex-col min-h-full font-sans antialiased">
  ${header}
  <div class="flex-1 flex flex-col">
    ${bodyContent}
  </div>
  ${footer}
  ${navScript}
</body>
</html>`;
}

function renderHomeScreen(input: PreviewInput): string {
  const featureCards = input.features
    .slice(0, 6)
    .map(
      (f) => `
      <div class="p-6 rounded-2xl bg-slate-900 border border-slate-800 hover:border-orange-500/50 transition duration-300">
        <div class="w-10 h-10 rounded-lg bg-orange-600/20 text-orange-400 mb-4 flex items-center justify-center font-bold">✓</div>
        <h3 class="text-lg font-bold text-white mb-2">${escapeHtml(f.feature_name)}</h3>
        <p class="text-slate-400 text-sm">Enterprise-grade implementation ready.</p>
      </div>`
    )
    .join("\n");

  return `
    <section class="max-w-5xl mx-auto px-6 py-20 text-center">
      <h1 class="text-6xl font-extrabold mb-6 tracking-tight bg-gradient-to-b from-white to-slate-400 bg-clip-text text-transparent">
        ${escapeHtml(input.appName)}
      </h1>
      <p class="text-lg text-slate-400 mb-10 max-w-2xl mx-auto leading-relaxed">
        ${escapeHtml(input.appDescription)}
      </p>
      <div class="flex gap-4 justify-center">
        <a data-nav="%2Fauth" class="rounded-xl bg-orange-600 hover:bg-orange-500 px-8 py-4 font-semibold transition shadow-lg shadow-orange-600/20 cursor-pointer">
          Get Started Free
        </a>
        <a data-nav="%2Fdashboard" class="rounded-xl border border-slate-800 hover:bg-slate-900 px-8 py-4 font-semibold transition cursor-pointer">
          View Demo Dashboard
        </a>
      </div>
    </section>

    <section class="max-w-5xl mx-auto px-6 py-12 border-t border-slate-900 w-full">
      <h2 class="text-3xl font-bold mb-8 text-center">System Capabilities</h2>
      <div class="grid gap-6 sm:grid-cols-2 md:grid-cols-3">
        ${featureCards}
      </div>
    </section>
  `;
}

