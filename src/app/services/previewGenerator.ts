function renderGenericScreen(input: PreviewInput, path: string): string {
  const currentRoute = input.routes.find((r) => r.route_path === path);
  const title = currentRoute ? currentRoute.page_name : "Custom Page";
  const desc = currentRoute ? currentRoute.purpose : `Content route: ${path}`;

  return `
    <div class="max-w-4xl w-full mx-auto px-6 py-20 flex-1 flex flex-col items-center justify-center text-center">
      <div class="w-16 h-16 rounded-full bg-slate-900 border border-slate-800 flex items-center justify-center text-2xl mb-6">📂</div>
      <h1 class="text-4xl font-bold mb-4">${escapeHtml(title)}</h1>
      <p class="text-lg text-slate-400 max-w-xl leading-relaxed mb-8">
        ${escapeHtml(desc || "This custom route has been planned and generated successfully.")}
      </p>
      <div class="p-6 bg-slate-900 border border-slate-800 rounded-xl max-w-md w-full font-mono text-left text-xs text-slate-400">
        <p class="text-orange-400 font-bold mb-2">// Route Configuration</p>
        <p>Route Path: <span class="text-white">${escapeHtml(path)}</span></p>
        <p>Access Level: <span class="text-white">Registered User</span></p>
        <p>File Location: <span class="text-white">app${escapeHtml(path)}/page.tsx</span></p>
      </div>
    </div>
  `;
}

function escapeHtml(text: string): string {
  return text
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}